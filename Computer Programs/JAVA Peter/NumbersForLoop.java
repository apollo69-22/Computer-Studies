class NumbersForLoop {
    public static void main(String[] args) {
        System.out.println("For Loop - Looping for 20 Times");
        System.out.println();
        
        for (int i = 1; i <= 20; i++) {             // You could use for (int i = 1; i < 21; i++)
            System.out.println("Number: " + i);
        }
    }
}